    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        TaiKhoan tk = null;
        KhachHang kh = listItem.get(tbl.getSelectedRow());
        for(TaiKhoan i : listtk){
            if(i.getMa_tai_khoan() == kh.getMa_tai_khoan()){
                tk = i;
                break;
            }
        }
        makh.setText(String.valueOf(kh.getMa_khach_hang()));
        hot.setText(kh.getHo_ten());
        sdt.setText(kh.getSo_dien_thoai());
        email.setText(kh.getEmail());
        if(tk == null) return;
        matk.setText(String.valueOf(tk.getMa_tai_khoan()));
    }//GEN-LAST:event_tblMouseClicked

    private void XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_XoaActionPerformed
        if(makh.getText().equals("") == true) return;
        int i = KhachHangDAO.delete(cons, listItem.get(tbl.getSelectedRow()));
        if(i == -1){
            JOptionPane.showMessageDialog(rootPane, "loi co so du lieu");
        }
        else if( i == 0){
            JOptionPane.showMessageDialog(rootPane, "khong tim duoc khach hang");
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "da xoa thanh cong");
            TaiKhoan tk = null;
            KhachHang kh = listItem.get(tbl.getSelectedRow());
            for(TaiKhoan j : listtk){
                if(j.getMa_tai_khoan() == kh.getMa_tai_khoan()){
                    tk = j;
                    break;
                }
            }
            listItem.remove(tbl.getSelectedRow());
            if(tk != null) listtk.remove(tk);
            dtm.removeRow(tbl.getSelectedRow());
            makh.setText("");
            hot.setText("");
            sdt.setText("");
            email.setText("");
            matk.setText("");
        }
    }//GEN-LAST:event_XoaActionPerformed

